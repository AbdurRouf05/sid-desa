const PocketBase = require('pocketbase/cjs');
const path = require('path');

// Load environment variables
const envPath = path.join(__dirname, '../.env.local');
const envConfig = require('dotenv').config({ path: envPath }).parsed || {};

async function main() {
    const pbUrl = envConfig.NEXT_PUBLIC_POCKETBASE_URL || 'http://127.0.0.1:8090';
    const adminEmail = envConfig.POCKETBASE_ADMIN_EMAIL;
    const adminPassword = process.argv[2] || envConfig.POCKETBASE_ADMIN_PASSWORD;

    if (!adminEmail || !adminPassword) {
        console.error("❌ Admin credentials missing.");
        process.exit(1);
    }

    const pb = new PocketBase(pbUrl);
    pb.autoCancellation(false);

    try {
        console.log(`🔌 Connecting to ${pbUrl}...`);
        await pb.admins.authWithPassword(adminEmail, adminPassword);

        console.log("🛠️ Upgrading 'social_feeds' schema...");

        try {
            const collection = await pb.collections.getOne('social_feeds');

            // Check if fields exist, if not add them
            const hasPinned = collection.fields.find(f => f.name === 'is_pinned');
            const hasThumbnail = collection.fields.find(f => f.name === 'thumbnail');

            let needsUpdate = false;

            if (!hasPinned) {
                console.log("   + Adding 'is_pinned' field");
                collection.fields.push({
                    name: 'is_pinned',
                    type: 'bool',
                    required: false
                });
                needsUpdate = true;
            }

            if (!hasThumbnail) {
                console.log("   + Adding 'thumbnail' field");
                collection.fields.push({
                    name: 'thumbnail',
                    type: 'file',
                    maxSelect: 1,
                    maxSize: 5242880,
                    mimeTypes: ['image/jpeg', 'image/png', 'image/webp']
                });
                needsUpdate = true;
            }

            if (needsUpdate) {
                await pb.collections.update('social_feeds', collection);
                console.log("✅ Schema updated successfully.");
            } else {
                console.log("   - Schema already up to date.");
            }

        } catch (e) {
            console.error("❌ Failed to update schema:", e.message);
            // If collection doesn't exist, we should create it (but audit said it exists)
        }

        console.log("🌱 Seeding Social Feeds...");

        // Clear existing (optional, but good for reset)
        const existing = await pb.collection('social_feeds').getFullList();
        if (existing.length === 0) {
            const seeds = [
                {
                    platform: "instagram",
                    url: "https://www.instagram.com/p/DFyG_4TPQ45/",
                    caption: "Kunjungan Silaturahmi PCNU Lumajang ke Kantor Pusat BMT NU.",
                    is_active: true,
                    is_pinned: true,
                    embed_code: "" // Will be auto-generated by frontend or magic engine later
                },
                {
                    platform: "tiktok",
                    url: "https://www.tiktok.com/@bmtnulumajang/video/7325608920138",
                    caption: "Cara mudah buka rekening via WhatsApp! 📱",
                    is_active: true,
                    is_pinned: false
                },
                {
                    platform: "youtube",
                    url: "https://www.youtube.com/watch?v=Hu4Y3-5R-qU",
                    caption: "Company Profile BMT NU Lumajang 2024 (Official)",
                    is_active: true,
                    is_pinned: true,
                },
                {
                    platform: "facebook",
                    url: "https://www.facebook.com/bmtnulumajang/posts/123456789",
                    caption: "Kegiatan Jumat Berkah Berbagi Nasi Bungkus.",
                    is_active: true,
                    is_pinned: false
                }
            ];

            for (const data of seeds) {
                await pb.collection('social_feeds').create(data);
                console.log(`   + Created: [${data.platform}] ${data.caption}`);
            }
        } else {
            console.log("   - Data already exists, skipping seed.");
        }

        console.log("✨ Social Feeds ready!");

    } catch (e) {
        console.error("❌ Error:", e);
    }
}

main();
